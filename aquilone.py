import turtle
t=turtle.Turtle()
s=turtle.Screen()
t.color("pink")
t.forward(150)
t.right(90)
t.color("brown")
t.begin_fill()
t.forward(150)
t.right(90)
t.forward(150)
c=150
i=(c**2+c**2)**(1/2)
t.right(135)
t.forward(i)
t.end_fill()
t.backward(i)
t.color("pink")
t.begin_fill()
t.left(45)
t.forward(c)
t.right(90)
t.forward(c)
t.right(135)
t.forward(i)
t.end_fill()
t.color("black")
t.circle(radius=80,extent=60)
t.circle(radius=-80,extent=60)
s.mainloop()